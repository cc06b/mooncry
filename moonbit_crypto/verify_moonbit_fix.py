def mix_columns(state):
    for i in range(4):
        s0, s1, s2, s3 = state[0][i], state[1][i], state[2][i], state[3][i]
        state[0][i] = gmul(0x02, s0) ^ gmul(0x03, s1) ^ s2 ^ s3
        state[1][i] = s0 ^ gmul(0x02, s1) ^ gmul(0x03, s2) ^ s3
        state[2][i] = s0 ^ s1 ^ gmul(0x02, s2) ^ gmul(0x03, s3)
        state[3][i] = gmul(0x03, s0) ^ s1 ^ s2 ^ gmul(0x02, s3)

def inv_mix_columns(state):
    for i in range(4):
        s0, s1, s2, s3 = state[0][i], state[1][i], state[2][i], state[3][i]
        state[0][i] = gmul(0x0e, s0) ^ gmul(0x0b, s1) ^ gmul(0x0d, s2) ^ gmul(0x09, s3)
        state[1][i] = gmul(0x09, s0) ^ gmul(0x0e, s1) ^ gmul(0x0b, s2) ^ gmul(0x0d, s3)
        state[2][i] = gmul(0x0d, s0) ^ gmul(0x09, s1) ^ gmul(0x0e, s2) ^ gmul(0x0b, s3)
        state[3][i] = gmul(0x0b, s0) ^ gmul(0x0d, s1) ^ gmul(0x09, s2) ^ gmul(0x0e, s3)

def add_round_key(state, round_keys, round_num):
    for j in range(4):
        for i in range(4):
            state[i][j] ^= round_keys[round_num * 4 + j][i]

def key_expansion(key):
    Nk = len(key) // 4
    Nr = 10 if Nk == 4 else (12 if Nk == 6 else 14)
    
    w = []
    for i in range(Nk):
        w.append([key[4*i], key[4*i+1], key[4*i+2], key[4*i+3]])
    
    for i in range(Nk, 4 * (Nr + 1)):
        temp = w[i-1][:]
        
        if i % Nk == 0:
            temp = [S_BOX[temp[1]], S_BOX[temp[2]], S_BOX[temp[3]], S_BOX[temp[0]]]
            temp[0] ^= RCON[i // Nk]
        elif Nk > 6 and i % Nk == 4:
            temp = [S_BOX[b] for b in temp]
        
        w.append([w[i-Nk][j] ^ temp[j] for j in range(4)])
    
    return w

def encrypt_block(block, round_keys, nr):
    state = bytes_to_state(block)
    
    add_round_key(state, round_keys, 0)
    
    for round in range(1, nr):
        sub_bytes(state)
        shift_rows_fixed(state)
        mix_columns(state)
        add_round_key(state, round_keys, round)
    
    sub_bytes(state)
    shift_rows_fixed(state)
    add_round_key(state, round_keys, nr)
    
    return state_to_bytes(state)

def decrypt_block(block, round_keys, nr):
    state = bytes_to_state(block)
    
    add_round_key(state, round_keys, nr)
    
    for round in range(nr - 1, 0, -1):
        inv_shift_rows_fixed(state)
        inv_sub_bytes(state)
        add_round_key(state, round_keys, round)
        inv_mix_columns(state)
    
    inv_shift_rows_fixed(state)
    inv_sub_bytes(state)
    add_round_key(state, round_keys, 0)
    
    return state_to_bytes(state)

def pkcs7_pad(data, block_size):
    padding_len = block_size - (len(data) % block_size)
    return data + bytes([padding_len] * padding_len)

def pkcs7_unpad(data):
    padding_len = data[-1]
    if padding_len > 16 or padding_len < 1:
        return data
    return data[:-padding_len]

def encrypt_ecb(data, key):
    nr = 10 if len(key) == 16 else (12 if len(key) == 24 else 14)
    round_keys = key_expansion(key)
    padded = pkcs7_pad(data, 16)
    
    encrypted = b''
    for i in range(0, len(padded), 16):
        encrypted += encrypt_block(padded[i:i+16], round_keys, nr)
    return encrypted

def decrypt_ecb(data, key):
    nr = 10 if len(key) == 16 else (12 if len(key) == 24 else 14)
    round_keys = key_expansion(key)
    
    decrypted = b''
    for i in range(0, len(data), 16):
        decrypted += decrypt_block(data[i:i+16], round_keys, nr)
    return pkcs7_unpad(decrypted)

